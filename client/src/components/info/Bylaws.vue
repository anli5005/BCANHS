<template>
  <div>
    <b-container class="text-light">
      <h4 class="display-4">Chapter Bylaws</h4>

      <h6 class="display-6">ARTICLE I: NAME</h6>
      <p>
        The name of this chapter shall be the Bergen County Academies Chapter of
        the National Honor Society.
      </p>
      <h6 class="display-6">ARTICLE II: PURPOSE</h6>
      <p>
        The purpose of this chapter shall be to create an enthusiasm for
        scholarship, to stimulate a desire to render service, to promote worthy
        leadership, and to encourage the development of character in students of
        Bergen County Academies.
      </p>

      <h6 class="display-6">ARTICLE III: POWERS</h6>
      <ol>
        <li>
          This chapter operates under the direction of and in full compliance
          with the NHS National Constitution. See www.nhs.us/constitutions.
        </li>
        <li>
          The chapter adviser is given the authority to supervise the
          administration of chapter activities, as delegated by the school
          principal.
        </li>
        <li>
          Final authority on all activities and decisions of the chapter resides
          with the school principal. (See Article V, Section 1 of the National
          Constitution.)
        </li>
        <li>
          Nondiscrimination. Our chapter of NHS maintains policies and practices
          that are designed to prevent discrimination against any qualified
          candidate or member on the basis of race, color, religion, ancestry,
          national origin, sex (gender), and disability. This policy of
          nondiscrimination applies to all practices, including the chapter
          administration and the selection, discipline, and dismissal of
          members.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE IV: MEMBERSHIP</h6>
      <ol>
        <li>
          Membership in this chapter is an honor bestowed upon deserving
          students by the faculty and shall be based on the criteria of
          Scholarship, Service, Leadership, and Character. Membership in the
          National Honor Society is a privilege, not a right.
        </li>
        <li>
          Membership in this chapter shall be known as active, honorary, and
          graduate. Active members become graduate members at graduation.
          Honorary members are selected at the discretion of the Faculty
          Council. Graduate and honorary members have no voice or vote in
          chapter affairs.
        </li>
        <li>
          Eligibility:
          <ol>
            <li>
              Candidates eligible for selection to this chapter must be members
              of the junior or senior class.
            </li>
            <li>
              Candidates eligible for selection to this chapter shall have a
              minimum cumulative grade point average of 3.500 on a 4.000 scale.
            </li>
            <li>
              Candidates eligible for selection to this chapter shall have
              completed at least 40 hours of community service (only 10 hours
              can be completed in-house) as reflected on the school’s community
              service database.
            </li>
            <li>
              Upon meeting the grade level, enrollment, and GPA standards,
              candidates shall then be considered based on their demonstration
              of service, leadership, and character in their Candidate Form.
            </li>
          </ol>
        </li>
      </ol>

      <h6 class="display-6">ARTICLE V: SELECTION OF MEMBERS</h6>
      <ol>
        <li>
          The selection of members to this chapter shall be by a majority vote
          of the Faculty Council which consists of five faculty members
          appointed by the principal. The chapter adviser shall be the sixth,
          nonvoting, exofficio member of the Faculty Council.
        </li>
        <li>
          The selection of active members shall be held once a year during the
          first trimester of the school year.
        </li>
        <li>
          Prior to the final selection, the following shall occur:
          <ol>
            <li>
              Students’ academic records shall be reviewed to determine
              scholastic eligibility.
            </li>
            <li>
              All students who are eligible scholastically (i.e., candidates)
              shall be notified and asked to complete and submit the Candidate’s
              Form with essay responses for further consideration.
            </li>
            <li>
              The faculty shall be requested to evaluate candidates determined
              to be scholastically eligible using the official evaluation form
              provided by the chapter adviser.
            </li>
            <li>
              The Faculty Council shall review the Candidate Forms, faculty
              evaluations and other relevant information to determine those who
              fully meet the selection criteria for membership.
            </li>
          </ol>
        </li>
        <li>
          Candidates become members when inducted at a special ceremony. There
          will be one induction ceremony per year. The fall inductions will
          include juniors and seniors only.
        </li>
        <li>
          An active member of the National Honor Society who transfers from this
          school will be given an official letter indicating the status of
          his/her membership.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE VI: OBLIGATION OF MEMBERS</h6>
      <ol>
        <li>
          Membership dues for this chapter shall be $20. Dues will be paid on a
          one-time basis and must be paid within two weeks of induction.
        </li>
        <li>
          Each member of this chapter who is in good standing with regard to the
          membership standards and member obligations shall be granted the
          privilege to wear the emblem adopted by the National Honor Society.
        </li>
        <li>
          Chapter members who are seniors in good standing shall be granted the
          privilege of wearing the honor stole at graduation.
        </li>
        <li>
          Members are required to complete the required amount of tutoring per
          year (10 hours) and complete at least one group service project per
          year.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE VII: OFFICERS</h6>
      <ol>
        <li>
          The officers of the chapter shall be president, vice president,
          secretary, treasurer, historian, webmaster, induction director,
          tutoring director, community service director, and fundraising
          director.
        </li>
        <li>
          Student officers shall be elected at the beginning of the third
          trimester. All junior members in good standing with the chapter are
          eligible to run for a position as an officer.
        </li>
        <li>
          Voting shall be by secret ballot. A majority vote shall be necessary
          to elect any officer of this chapter. If the first vote does not yield
          a majority, a second vote shall be taken of the two candidates
          receiving the highest number of votes.
        </li>
        <li>
          It shall be the duty of the President to preside at the meetings of
          the chapter, and serve as the official representative of the chapter
          at school and community functions.
        </li>
        <li>
          The Vice President shall preside in the absence of the president and
          shall also keep a record of members’ contributions to leadership and
          service.
        </li>
        <li>
          The Secretary shall keep the minutes and attendance records for
          meetings and be responsible for all official correspondence.
        </li>
        <li>
          The Treasurer shall keep the record of business expenses, dues, and
          all other financial transactions of the chapter.
        </li>
        <li>
          The Historian will compile memorabilia, primarily pictures, throughout
          the school year to chronicle NHS activities.
        </li>
        <li>
          The Webmaster will primarily be in charge of the NHS website, in
          addition to any electronic needs of the class throughout the year.
        </li>
        <li>
          The Induction Director will be responsible for the planning and
          coordinating of the NHS induction in December, with the assistance of
          the advisor and the rest of the NHS Council.
        </li>
        <li>
          The Tutoring Director will be responsible for matching tutors with
          students who need tutoring.
        </li>
        <li>
          The Community Service Director will be responsible for making sure
          every member has met the community service requirements for NHS and
          BCA.
        </li>
        <li>
          The Fundraising Director will be responsible for ensuring the success
          of monthly NHS bake sales by ensuring there are enough supplies to
          sell, staffing all bake sales, and notifying the school of all bake
          sales.
        </li>
        <li>
          Officers and the faculty adviser(s) shall collectively be known as the
          chapter’s Executive Committee. The executive committee shall establish
          annual goals for the chapter and have general charge of the meetings
          and the business of the chapter. Any action by the executive committee
          is subject to the review of the chapter members.
        </li>
        <li>
          Officers who do not carry out their duties will be provided a written
          warning. If the officer continues to neglect his/her duties, the
          officer will be relieved of their office duties by the Chapter
          Adviser. In this case, the executive committee will begin a search and
          fill the vacancies among officers.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE VIII: MEETINGS</h6>
      <ol>
        <li>
          Regular meetings of this chapter shall be weekly before school. Weekly
          meetings are open to all NHS members. Special meetings can be called
          by the president with approval of the adviser.
        </li>
        <li>
          This chapter shall conduct its meetings according to Robert’s Rules of
          Order.
        </li>
        <li>
          Executive committee members are expected to attend all regular
          meetings. All members are expected to attend one chapter meeting. If
          members do not attend meetings, the member may be subject to
          discipline and/or dismissal.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE IX: ACTIVITIES</h6>
      <ol>
        <li>
          The chapter shall determine one or more service projects for each
          year.
        </li>
        <li>All members shall regularly participate in these projects.</li>
        <li>
          These projects shall have the following characteristics: fulfill a
          need within the school or community, have the support of the
          administration and the faculty, be appropriate and educationally
          defensible, and be well-planned, organized, and executed.
        </li>
        <li>
          Each member shall have the responsibility for choosing and
          participating in an individual service project which reflects his or
          her particular talents and interests and is approved by the chapter
          adviser. This is in addition to the chapter projects to which all
          members contribute.
        </li>
        <li>
          The chapter shall publicize and promote its projects in a positive
          manner.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE X: DISCIPLINE and DISMISSAL of MEMBERS</h6>
      <ol>
        <li>
          Any member who falls below the standards of scholarship, leadership,
          character, or service may be considered for discipline or dismissal
          from the Bergen County Academies chapter of the National Honor
          Society. A member of the National Honor Society is expected to
          maintain his/her academic standing and take an active role in service
          and leadership in his/her school and community.
        </li>
        <li>
          If a member’s cumulative grade point average falls below the standard
          in effect when he/she was selected (3.500 GPA), he /she will be given
          a written warning and a reasonable time period for improvement. If the
          cumulative grade point average remains below standard at the end of
          the warning period the student will be subject to further disciplinary
          action by the Faculty Council that can include consideration of
          dismissal from the chapter.
        </li>
        <li>
          Violations of the law or school regulations can result in immediate
          consideration of the dismissal of a member (see
          <b>Section 5</b> below). These violations include, but are not limited
          to DWI, stealing, destruction of property, cheating, truancy, or
          possession, selling, or being under the influence of drugs or alcohol
          at school or school-related activities or in the community.
        </li>
        <li>
          Offenders of the school conduct code (such as use of profanity,
          failure to comply, unexcused absences, excessive tardiness, etc.) will
          receive a written warning notification. A conference may be requested
          by either party (Chapter Adviser or student/parent). If the member is
          involved in another violation of the school conduct code, the member
          may be considered for dismissal.
        </li>
        <li>
          Chapter officers, as representatives of the chapter, can be removed
          from their positions as a consequence of disciplinary action taken by
          the Chapter Adviser.
        </li>
        <li>
          In all cases of pending dismissal:
          <ol>
            <li>
              The member will receive written notification from the Chapter
              Adviser indicating the reason for consideration of dismissal.
            </li>
            <li>
              The member will be given the opportunity to respond to the
              charge(s) against him/her at a hearing before a panel of five
              Faculty Council members prior to any vote on dismissal (in
              accordance with due process identified in Article X of the
              National Constitution). The member has the opportunity to present
              his/her defense either in person or via a written statement
              presented in lieu of the face-to-face hearing. Following the
              hearing, the Faculty Council members will then vote on whether to
              dismiss. A majority vote of the Faculty Council members is needed
              to dismiss any member.
            </li>
            <li>
              The results of the Faculty Council vote will be presented to the
              principal for review, and then stated in a letter sent to the
              student and parents. Dismissed members must surrender any
              membership emblems to the adviser.
            </li>
            <li>
              The Faculty Council’s decision may be appealed to the building
              principal and afterwards according to provisions of the school
              district discipline policies.
            </li>
            <li>
              A member who is dismissed or resigns, may never again be
              considered for membership in the National Honor Society.
            </li>
          </ol>
        </li>
        <li>
          In lieu of dismissal, the Faculty Council may impose disciplinary
          sanctions upon a member as deemed appropriate.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE XI: RATIFICATION, APPROVAL, and REVIEW</h6>
      <ol>
        <li>
          These bylaws will be approved upon receiving a two-thirds affirmative
          vote of all active members of the chapter, a majority affirmative vote
          of the Faculty Council, and approval by the administration.
        </li>
        <li>
          These bylaws shall be reviewed and if necessary, revised within five
          years from the date of approval noted on this document.
        </li>
      </ol>

      <h6 class="display-6">ARTICLE XII: AMENDMENTS</h6>
      <p>
        These bylaws may be amended by a two-thirds vote of the chapter,
        provided notice of the proposed amendment has been given to members at
        least one month prior to the vote. The exceptions are Articles V and X,
        which are developed by the Faculty Council with the approval of the
        principal (selection and discipline).
      </p>
